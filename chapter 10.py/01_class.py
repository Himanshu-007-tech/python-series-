class Employee:
    language = "py"# This is s class atributes
    salary = "12000"

Himanshu = Employee()
Himanshu.name = "Himanshu"# THIS is an instance attributes
print(Himanshu.name, Himanshu.language, Himanshu.salary)

Ravi = Employee()
Ravi.name = "Ravi Roro Robinson"
print(Ravi.name, Ravi.salary, Ravi.language)

